def stringify(value):
    if isinstance(value, dict):
        return json.dumps(value)
    elif isinstance(value, list):
        return json.dumps(value)
    else:
        return value

def sane_batch_map(fn: Callable[[list[dict[str, any]]], any], empty: dict[str,list]) -> Callable[[dict[str,list]], any]:
    def batch_map(batch: dict[str,list]) -> any:
        results = []
        batch = [dict(zip(batch,t)) for t in zip(*batch.values())]
        for example in batch:
            try:
                results = results + fn(example)
            except Exception as e:
                print(example)
                print(traceback.format_exc())
        return {k: [stringify(dic[k]) if k in dic else 'null' for dic in results] for k in empty} if len(results) else empty

    return batch_map

    
def find_all_xml_tags(text: str, tag: str) -> list[str]:
    """ finds all xml tags in the text, excluding the tags themselves, empty lists are returned if no tags are found """
    start_tag = f'<{tag}>'
    end_tag = f'</{tag}>'
    tags = []
    start = 0
    while True:
        start = text.find(start_tag, start)
        if start == -1:
            break
        start += len(start_tag)
        end = text.find(end_tag, start)
        if end == -1:
            break
        tags.append(text[start:end].strip())
        start = end + len(end_tag)
    return tags

